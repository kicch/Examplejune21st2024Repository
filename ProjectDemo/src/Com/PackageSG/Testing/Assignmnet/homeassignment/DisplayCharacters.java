package Com.PackageSG.Testing.Assignmnet.homeassignment;

public class DisplayCharacters {
    public static void main(String[] args) {
        displayCharacters();
    }

    public static void displayCharacters() {
        // Loop through characters from 'p' to 'z'
        for (char ch = 'p'; ch <= 'z'; ch++) {
            System.out.print(ch + " ");
        }
    }
}
